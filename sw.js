const CACHE_NAME = 'bsu-premium-v1';
const urlsToCache = [
  '/Bsu-premium-calculator/',
  '/Bsu-premium-calculator/index.html',
  '/Bsu-premium-calculator/manifest.json',
  '/Bsu-premium-calculator/icons/icon-192.png',
  '/Bsu-premium-calculator/icons/icon-512.png'
];

self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => cache.addAll(urlsToCache))
  );
});

self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => response || fetch(event.request))
  );
});

self.addEventListener('activate', event => {
  const cacheWhitelist = [CACHE_NAME];
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (!cacheWhitelist.includes(cacheName)) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});
